
package src.logicalprogram;

public class SquareRoot {
	
	// 'Math' is the java keyword which is used to perform various mathematical operations 
   // For the square root the syntax is  'Math,sqrt(a)'   where a is the variable we declared
  // the data type of the declared variable should be in the form of double as results may contain decimal values
 // the declared variable can be local or global
	
	public static void main(String[] args) 
	{
		double a=Math.sqrt(9);            
		System.out.println(a);
	}	
}

