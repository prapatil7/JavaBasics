package src.logicalprogram;

public class SwapNumber {

public static void main(String[] args)
{
	
	int a=25;
	int b=50;
	
//    a=a+b       25+50=75
//    b=a-b       75-50=15
//    a=a-b       75-15=60
//using swap we can change the values of integers
 	
 
    int c;
    c=a;
    a=b;
    b=c;
    System.out.println("a= "+ a);
    System.out.println("b= "+b);	
	
}
}
	
	
	
	
	
	
	
	
	
	

	
	
	

