package switchcase;

public class DaysSwitchCaseStatement {
	public static void main(String[] args) {
		
		int day= 3;
		switch(day) {
		
		case 1:
			System.out.println("monday");
		
		case 2:
			System.out.println("tuesday");
		
		case 3:
			System.out.println("wednesday");
		
		
		
		
		
		
		
	}

}
}