package revision;

public class DoWhile {
	public static void main(String[] args) {
		
	
	int i=1;
	do
	{
		if(i%7==0)
		{
			System.out.println("no.s divisible by i are "+ i );
		}
		else
		{
			System.out.println("no.s are not divisible by 7 "+ i);
		}
		i++;
	}
		while(i<=100);
	
	}
}
		

	
