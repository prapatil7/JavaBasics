package revision;

public class DataTypes {
	
	// primitive data types
	// numerics: byte, short, int , long 
	
public static void main(String[] args)
{

	byte a = 48;
	byte b = 53;
	int  c = a + b;
	System.out.println(c);
	
	short d= 13;
	short e = 56;
	int f = d-e; 
	System.out.println("the difference is " + f);         // concatination
	
	int p= 123;
	int q= 248;
	int multiplication = p*q;
	System.out.println("the multiplication of two no.s is " + multiplication);
	
	long o= 45516l;
	long m = 23l;
	long n = o/m;
	System.out.println("the division is "+n);
	
	// floating point
	
	float s= 78.35f;
	float h = 8789.25f;
	float j = s*h;
	
	double t=442458420.85;
	double r=1215445.248;
	double w= t+r;
	System.out.println(w);			
}	
}	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	