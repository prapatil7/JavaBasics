package mock_practice_1;

public class Arrays {
	public static void main(String[] args) {
		
		  int [] w=new int [4];
		     
		  w[0]=10;
		  w[1]=255;
		  w[2]=155;
		  w[3]=147;
		 
		  
		 for (int e: w)
		 {
			 System.out.println(e);
		 }		
		 
		 for (int i=0; i<=w.length-1; i++)
		 {
			 System.out.println(w[i ]);
		 }
	}
}
