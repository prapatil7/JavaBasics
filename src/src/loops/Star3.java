package loops;

public class Star3 {
	
	public static void main(String[] args)
	{
	    for(int i=1; i<=5; i++)
	    {
	    	for (int j=1; i<=5; j++)
	    	{
	            if(j<=(6-i))
	            {
	            	System.out.println("*");
	            }
	            else
	            {
	            	System.out.println(" ");
	            }
	    	}
	    }
		
	}
}

	
	
	
	
	
	
	
	
	
	

