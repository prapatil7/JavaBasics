package oops_inheritance;

public class Father {
	
	public static void money()
	{
		System.out.println("money");
	}

	public void car()
	{
		System.out.println("car");
	}
	
	public void home()
	{
		System.out.println("home");
	}
	
	public static void main(String[] args)
	{
	    Father k=new Father();
	    k.money();
	    k.car();
	    k.home();
		 
		
		
	}
}
