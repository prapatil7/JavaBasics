package assignments;

public class DataTypes 
{
  public static void main(String[] args) {
	// integral data types
	  byte a= 100;
	  int b= 12560;
	 int k = 123;
	  int c= a+b+k;
	  System.out.println("The sum is " + c);
	  
	  // float data types
	  
	  double d= 12.50;
	  double e= 0.125;
	  double f=d*e;
	  System.out.println(f);
	  
	  //char data type
	  
	  char g='A';
	  char group = 'B';
	  System.out.println("Sections - " + g +" , " + group);
	  
	  //booleans 
	  
	  boolean h = true;
	  boolean i = false;
	
	  System.out.println(h);
	  System.out.println(i);
	 
    // strings
	  
	  String l= "My name is Pratik \n pratik";
	  System.out.println(l);
}
	
	
	
	
	
	

}
