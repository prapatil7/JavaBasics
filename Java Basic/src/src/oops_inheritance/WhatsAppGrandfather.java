package oops_inheritance;

public class WhatsAppGrandfather {
	// the child class for this method is WAChild1 class 
	// and the child class of WAChild1 class is WAChild 2
	// These are multilevel type inheritance 
	// multilevel means 2,3,... generations of parent class--childclass--child2class......
	public void sms()
	{
		System.out.println("sms");
	}
    
	public static void story()
	{
		System.out.println("story");
	}
}
